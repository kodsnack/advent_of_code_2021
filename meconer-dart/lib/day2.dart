import 'package:flutter/material.dart';

class Day2 extends StatelessWidget {
  const Day2({Key? key}) : super(key: key);
  static String routeName = 'day2';
  final bool isExample = false;

  @override
  Widget build(BuildContext context) {
    final resultPart1 = doDay2Part1();
    final resultPart2 = doDay2Part2();
    return Scaffold(
      appBar: AppBar(
        title: const Text('Day 2 Dive!'),
      ),
      body: Center(
        child: Column(
          children: [
            const SizedBox(
              height: 200,
            ),
            SelectableText('Svar dag 2 del 1: $resultPart1'),
            SelectableText('Svar dag 2 del 2 : $resultPart2'),
          ],
        ),
      ),
    );
  }

  int doDay2Part1() {
    int horizontalPosition = 0;
    int depth = 0;
    final List<String> day2Input = getDay2Input(isExample);
    for (String line in day2Input) {
      final commandList = line.split(' ');
      String command = commandList[0];
      String value = commandList[1];
      switch (command) {
        case 'forward':
          {
            horizontalPosition += int.parse(value);
          }
          break;
        case 'down':
          {
            depth += int.parse(value);
          }
          break;
        case 'up':
          {
            depth -= int.parse(value);
          }
          break;
      }
    }
    return horizontalPosition * depth;
  }

  int doDay2Part2() {
    int horizontalPosition = 0;
    int depth = 0;
    int aim = 0;
    final List<String> day2Input = getDay2Input(isExample);
    for (String line in day2Input) {
      final commandList = line.split(' ');
      String command = commandList[0];
      String value = commandList[1];
      switch (command) {
        case 'forward':
          {
            int forwardVal = int.parse(value);
            horizontalPosition += forwardVal;
            depth += aim * forwardVal;
          }
          break;
        case 'down':
          {
            aim += int.parse(value);
          }
          break;
        case 'up':
          {
            aim -= int.parse(value);
          }
          break;
      }
    }
    return horizontalPosition * depth;
  }
}

